<?php
namespace TNotifyer\Providers;

use PHPUnit\Framework\TestCase;
use \DateTimeInterface;
use \DateTime;

class SchedulerTest extends TestCase
{

    /**
     * @dataProvider nextDataProvider
     */
    public function testNext($schedule, $time, $next)
    {
        $value = (new DateTime($next))->format(DateTimeInterface::ATOM);
        $result = (new Scheduler($schedule))->next(new DateTime($time))->format(DateTimeInterface::ATOM);

        $this->assertEquals($value, $result);
    }

    public function nextDataProvider()
    {
        return [
            ['*', '2026-01-01 00:00', '2026-01-01 00:01'],
            ['*/2', '2026-01-01 00:00', '2026-01-01 00:02'],
            ['*/2', '2026-01-01 00:59', '2026-01-01 01:00'],
            ['*/2', '2026-01-01 23:59', '2026-01-02 00:00'],
            ['5,15,45', '2026-01-01 00:00', '2026-01-01 00:05'],
            ['5,15,45', '2026-01-01 00:30', '2026-01-01 00:45'],
            ['5,15,45', '2026-01-01 00:45', '2026-01-01 01:05'],
            ['10:30', '2026-01-01 10:29', '2026-01-01 10:30'],
            ['10:30', '2026-01-01 10:30', '2026-01-02 10:30'],
            ['10:*/5', '2026-01-01 00:00', '2026-01-01 10:00'],
            ['10:*/5', '2026-01-01 10:30', '2026-01-01 10:35'],
            ['10:*/5', '2026-01-01 11:00', '2026-01-02 10:00'],
            ['10,20:10', '2026-01-01 00:00', '2026-01-01 10:10'],
            ['10,20:10', '2026-01-01 12:00', '2026-01-01 20:10'],
            ['*/5:00', '2026-01-01 00:00', '2026-01-01 05:00'],
            ['*/5:00', '2026-01-01 12:00', '2026-01-01 15:00'],
            ['*/5:59', '2026-01-01 20:58', '2026-01-01 20:59'],
            ['*/5:59', '2026-01-01 21:00', '2026-01-02 00:59'],
        ];
    }

}
